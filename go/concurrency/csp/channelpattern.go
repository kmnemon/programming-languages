func orChannel() {
	//takes in a variadic slice of channels and returns a single channel
	var or func(channels ...<-chan interface{}) <-chan interface{}
	or = func(channels ...<-chan interface{}) <-chan interface{} {
		switch len(channels) {
		case 0: //base case 1
			return nil
		case 1: //base case 2
			return channels[0]
		}

		orDone := make(chan interface{})
		go func() { //recursive case
			defer close(orDone)
			switch len(channels) {
			case 2: //optimization to keep the number of goroutines constrained.
				select {
				case <-channels[0]:
				case <-channels[1]:
				}
			default: //6
				select {
				case <-channels[0]:
				case <-channels[1]:
				case <-channels[2]:
				case <-or(append(channels[3:], orDone)...): //6
				}
			}
		}()
		return orDone
	}

	//apply the or channel
	sig := func(after time.Duration) <-chan interface{} {
		c := make(chan interface{})
		go func() {
			defer close(c)
			time.Sleep(after)
		}()
		return c
	}
	start := time.Now()
	<-or(
		sig(2*time.Hour),
		sig(5*time.Minute),
		sig(1*time.Hour),
		sig(1*time.Minute),
		sig(1*time.Second),
	)
	fmt.Printf("done after %v", time.Since(start))
}

